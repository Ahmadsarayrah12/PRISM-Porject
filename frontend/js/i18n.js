export function setLanguage(lang) {
    localStorage.setItem('prism_lang', lang);
    document.documentElement.lang = lang;
    document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
    applyTranslations(lang);
}

export function getLanguage() {
    return localStorage.getItem('prism_lang') || 'ar';
}

export function applyTranslations(lang) {
    const dict = translations[lang];
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (dict[key]) {
            el.innerHTML = dict[key];
        }
    });

    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        if (dict[key]) {
            el.placeholder = dict[key];
        }
    });

    document.querySelectorAll('[data-i18n-title]').forEach(el => {
        const key = el.getAttribute('data-i18n-title');
        if (dict[key]) {
            el.title = dict[key];
        }
    });
    
    if (document.title.includes('Workspace') || document.title.includes('مساحة')) {
        document.title = dict["page.app.title"];
    } else {
        document.title = dict["page.home.title"];
    }

    // Force re-render of dynamic tool title if elements are available
    const activeTool = document.querySelector('.tool-btn.active');
    if (activeTool) {
        const titleKey = activeTool.getAttribute('data-title-key');
        const descKey = activeTool.getAttribute('data-desc-key');
        const currentToolTitle = document.getElementById('current-tool-title');
        const newsInput = document.getElementById('news-input');
        if (currentToolTitle && titleKey) currentToolTitle.innerHTML = dict[titleKey];
        if (newsInput && descKey) newsInput.placeholder = dict[descKey];
    }
}
